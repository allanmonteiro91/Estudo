/* Desafio
- Crie uma variável string com um número dentro (ex: "10")
- Converta para número e faça uma operação matemática
- Mostre um exemplo de coerção automática (sem conversão)
- Mostre um exemplo de conversão explícita (usando Number() ou String())
*/

//declarar string com um número
let numero = '10';

//operação matemática + conversão explicita
console.log(Number(numero) + 10);

//exemplo de conversão automática
console.log(numero * 10);

